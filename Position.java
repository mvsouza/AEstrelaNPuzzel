public class Position
{
	private int y;
	private int x;
	/**
	 * Returns the x value from this position.
	 * @return
	 */
	public int getX()
	{
		return x;
	}
	/**
	 * Returns the y value from this position.
	 * @return
	 */
	public int getY()
	{
		return y;
	}
	public Position(int x,int y)
	{
		this.x=x;
		this.y=y;
	}
}